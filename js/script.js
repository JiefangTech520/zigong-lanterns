// 加载效果图
function loadGalleryImages() {
    // 根据分类过滤图片
    let filteredImages = currentCategory === 'all'
        ? galleryImages
        : galleryImages.filter(img => img.category === currentCategory);

    const start = galleryPage * galleryPerPage;
    const end = start + galleryPerPage;
    const imagesToShow = filteredImages.slice(start, end);

    imagesToShow.forEach((image, index) => {
        const galleryItem = document.createElement('div');
        galleryItem.className = 'gallery-item';
        galleryItem.style.animationDelay = `${index * 0.1}s`;
        galleryItem.innerHTML = `
            <img src="${image.src}" alt="${image.title}" loading="lazy">
            <div class="gallery-overlay">
                <h3>${image.title}</h3>
                <p>${getCategoryTranslation(image.category)}</p>
            </div>
        `;

        galleryItem.addEventListener('click', () => openModal(image.src, image.title));
        galleryGrid.appendChild(galleryItem);
    });

    galleryPage++;

    // 隐藏加载更多按钮如果没有更多图片
    if (end >= filteredImages.length) {
        loadMoreBtn.style.display = 'none';
    } else {
        loadMoreBtn.style.display = 'inline-block';
    }
}

