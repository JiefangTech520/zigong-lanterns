let currentLang = 'zh';

// 切换语言函数
function changeLanguage(lang) {
    currentLang = lang;

    // 更新页面标题
    document.querySelector('title').textContent = getTranslation('meta.title');

    // 更新所有带data-i18n属性的元素
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.innerHTML = getTranslation(key);
    });

    // 更新所有带data-i18n-placeholder属性的元素
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.setAttribute('placeholder', getTranslation(key));
    });

    // 更新语言按钮状态
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    // 重新加载图片库以更新分类和标题
    reloadGallery();
    reloadCases();
}

// 获取翻译
function getTranslation(key) {
    const keys = key.split('.');
    let value = translations[currentLang];

    for (const k of keys) {
        if (value[k] !== undefined) {
            value = value[k];
        } else {
            console.warn(`Translation key not found: ${key}`);
            return key;
        }
    }

    return value;
}

// 获取分类翻译
function getCategoryTranslation(category) {
    return translations[currentLang].categories[category] || category;
}

